public class Coords {
    private int begin, height;


    public Coords(int begin, int height) {
        this.begin = begin;
        this.height = height;
    }


    public int getBegin() {
        return begin;
    }

    public int getHeight() {
        return height;
    }
    public void setHeight(int height) {
        this.height = height;
    }
}

